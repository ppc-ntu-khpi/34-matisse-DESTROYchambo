    private void ButtonReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonReportActionPerformed
        StringBuilder report = new StringBuilder();
        report.append("CUSTOMERS REPORT\n");
        report.append("================\n");

        for (int i = 0; i < Bank.getNumberOfCustomers(); i++) {
            Customer customer = Bank.getCustomer(i);
            report.append("\nCustomer: ").append(customer.getLastName())
                    .append(", ").append(customer.getFirstName()).append("\n");

            for (int j = 0; j < customer.getNumberOfAccounts(); j++) {
                com.mybank.domain.Account account = customer.getAccount(j);
                String type;
                if (account instanceof SavingsAccount) {
                    type = "Savings Account";
                } else if (account instanceof CheckingAccount) {
                    type = "Checking Account";
                } else {
                    type = "Unknown Account Type";
                }
                report.append("    ").append(type)
                        .append(": current balance is ")
                        .append(account.getBalance()).append("\n");
            }
        }

        jTextPane1.setText(report.toString());
    }//GEN-LAST:event_ButtonReportActionPerformed

