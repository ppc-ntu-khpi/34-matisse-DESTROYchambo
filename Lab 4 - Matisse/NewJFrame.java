    private void ButtonShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonShowActionPerformed
        Customer current = Bank.getCustomer(jComboBox1.getSelectedIndex());
        String accType = current.getAccount(0) instanceof CheckingAccount ? "Checking" : "Savings";
        String custInfo
                = current.getLastName() + " " + current.getFirstName() + ", customer #" + jComboBox1.getSelectedIndex()
                + "\n-------------------\n"
                + accType + " $"
                + current.getAccount(0).getBalance();
        jTextPane1.setText(custInfo);

    }//GEN-LAST:event_ButtonShowActionPerformed

